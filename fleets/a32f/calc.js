export function lookupA321Speeds(weightLbs, confFull = true) {
  const wKlbs = weightLbs / 1000;
  let lo, hi;
  if (wKlbs <= A321_SPEEDS[0][0]) { lo = hi = A321_SPEEDS[0]; }
  else if (wKlbs >= A321_SPEEDS[A321_SPEEDS.length-1][0]) { lo = hi = A321_SPEEDS[A321_SPEEDS.length-1]; }
  else {
    for (let i = 1; i < A321_SPEEDS.length; i++) {
      if (wKlbs <= A321_SPEEDS[i][0]) { lo = A321_SPEEDS[i-1]; hi = A321_SPEEDS[i]; break; }
    }
  }
  const t = lo === hi ? 0 : (wKlbs - lo[0]) / (hi[0] - lo[0]);
  const interp = (a, b) => Math.round(a + t*(b-a));
  return {
    VLS: confFull ? interp(lo[5], hi[5]) : interp(lo[4], hi[4]),
    F:   interp(lo[1], hi[1]),
    S:   interp(lo[2], hi[2]),
    O:   interp(lo[3], hi[3]),
  };
}

