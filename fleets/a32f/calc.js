// ─── SHARED BILINEAR INTERPOLATION ───────────────────────────────────────────
function bilinearInterp(table, pressureAlt, weightKlbs) {
  const alts = table.altitudes;
  const wgts = table.weights;
  const data = table.data;
  const alt = Math.max(alts[0], Math.min(alts[alts.length-1], pressureAlt));
  const wgt = Math.max(wgts[0], Math.min(wgts[wgts.length-1], weightKlbs));
  let a0 = 0, a1 = alts.length - 1;
  for (let i = 0; i < alts.length - 1; i++) {
    if (alt >= alts[i] && alt <= alts[i+1]) { a0 = i; a1 = i+1; break; }
  }
  let w0 = 0, w1 = wgts.length - 1;
  for (let i = 0; i < wgts.length - 1; i++) {
    if (wgt >= wgts[i] && wgt <= wgts[i+1]) { w0 = i; w1 = i+1; break; }
  }
  const tAlt = alts[a0] === alts[a1] ? 0 : (alt - alts[a0]) / (alts[a1] - alts[a0]);
  const tWgt = wgts[w0] === wgts[w1] ? 0 : (wgt - wgts[w0]) / (wgts[w1] - wgts[w0]);
  const v00 = data[a0][w0], v01 = data[a0][w1];
  const v10 = data[a1][w0], v11 = data[a1][w1];
  return v00*(1-tAlt)*(1-tWgt) + v01*(1-tAlt)*tWgt + v10*tAlt*(1-tWgt) + v11*tAlt*tWgt;
}

// RwyCC index: CC6=0, CC5=1, CC4=2, CC3=3, CC2=4, CC1=5
const CC_INDEX = { 6:0, 5:1, 4:2, 3:3, 2:4, 1:5 };

function calcFromTables(TABLE_MAP, { weightLbs, flap, brakeMode, pressureAlt, oatC, headwind, vappAdditive, reversers }) {
  const ccCols = [6,5,4,3,2,1];
  const table = TABLE_MAP[`${flap}|${brakeMode}`];
  if (!table) return null;
  const weightKlbs = weightLbs / 1000;
  const isaDev = oatC - (15 - 2 * pressureAlt / 1000);
  const distances = {};
  for (const cc of ccCols) {
    const ccIdx = CC_INDEX[cc];
    const singleCCTable = {
      altitudes: table.altitudes,
      weights:   table.weights,
      data:      table.data.map(row => row.map(cell => cell[ccIdx])),
    };
    let d = bilinearInterp(singleCCTable, pressureAlt, weightKlbs);
    if (headwind < 0)        d += table.twPerKt[ccIdx] * Math.abs(headwind);
    if (isaDev > 0)          d += table.isaAbovePer10[ccIdx] * (isaDev / 10);
    if (vappAdditive >= 15)  d += table.vappPlus15[ccIdx];
    else if (vappAdditive >= 10) d += table.vappPlus10[ccIdx];
    if (!reversers)          d += table.noReverser[ccIdx];
    distances[cc] = Math.round(d / 10) * 10;
  }
  return distances;
}

