// ─── SHARED CLIMB INTERPOLATION ──────────────────────────────────────────────
function _interpClimb(table, altCols, oatRows, pressureAlt, oatC) {
  const clampedOAT = Math.max(oatRows[0], Math.min(oatRows[oatRows.length-1], oatC));
  const clampedAlt = Math.max(altCols[0], Math.min(altCols[altCols.length-1], pressureAlt));
  let r0 = 0, r1 = oatRows.length - 1;
  for (let i = 0; i < oatRows.length - 1; i++) {
    if (clampedOAT >= oatRows[i] && clampedOAT <= oatRows[i+1]) { r0 = i; r1 = i+1; break; }
  }
  let c0 = 0, c1 = altCols.length - 1;
  for (let i = 0; i < altCols.length - 1; i++) {
    if (clampedAlt >= altCols[i] && clampedAlt <= altCols[i+1]) { c0 = i; c1 = i+1; break; }
  }
  const v00=table[r0][c0], v01=table[r0][c1], v10=table[r1][c0], v11=table[r1][c1];
  if (v00==null||v01==null||v10==null||v11==null) return null;
  const tR = oatRows[r0]===oatRows[r1] ? 0 : (clampedOAT-oatRows[r0])/(oatRows[r1]-oatRows[r0]);
  const tC = altCols[c0]===altCols[c1] ? 0 : (clampedAlt-altCols[c0])/(altCols[c1]-altCols[c0]);
  return Math.round((v00*(1-tR)*(1-tC)+v01*(1-tR)*tC+v10*tR*(1-tC)+v11*tR*tC)*10)/10;
}
