// Applies all AOM QRH corrections sequentially and returns { MAX_MAN, HI, MED, LO }
// Params match the E170/175 calcDistances signature where possible, plus brakingAction (ICAO code 1-6).
export function calcE190Distances({
  weightLbs,
  flap,
  ice,
  brakingAction = 6,
  pressureAlt   = 0,
  oatC          = 15,
  headwind      = 0,
  slopePct      = 0,
  vappAdditive  = 5,   // kt at threshold above Vref (AOM example uses VREF+5 as the baseline)
  reverser      = true,
}) {
  const flapKey = flap === "Full" ? "FLAP FULL" : "FLAP 5";
  const key     = `${flapKey}|${!!ice}|${brakingAction}`;
  const table   = E190_QRH[key] ?? E190_QRH["FLAP 5|false|6"];

  // ISA deviation at field elevation: ISA SL=15°C, lapse 2°C/1000ft
  const isaDev  = oatC - (15 - 2 * pressureAlt / 1000);
  const wDiff   = (weightLbs - E190_REF_WEIGHT) / 1000;

  const results = {};
  for (const [setting, v] of Object.entries(table)) {
    const [
      base,
      wBelowPer1k, wAbovePer1k,   // wAbovePer1k unused — overweightPer1k used instead
      altPer1k,
      isaBelowPer5, isaAbovePer5,
      hwPer5, twPer5,
      uphillPer1pct, downhillPer1pct,
      vrefPer5,
      revInop,
      overweightPer1k,
    ] = v;

    let d = base;

    // Weight correction
    if (wDiff < 0) d -= wBelowPer1k * Math.abs(wDiff);
    else if (wDiff > 0) d += overweightPer1k * wDiff;

    // Altitude correction
    d += altPer1k * (pressureAlt / 1000);

    // Temperature (ISA deviation) correction
    d += isaDev < 0
      ? -(isaBelowPer5 * Math.abs(isaDev) / 5)
      :  (isaAbovePer5 * isaDev / 5);

    // Wind correction (headwind positive = reduces distance)
    d += headwind >= 0
      ? -(hwPer5 * headwind / 5)
      :  (twPer5 * Math.abs(headwind) / 5);

    // Slope correction
    d += slopePct > 0
      ?  (uphillPer1pct  *  slopePct)
      : -(downhillPer1pct * Math.abs(slopePct));

    // VREF overspeed correction — column is "per 5 kt above VREF"
    // vappAdditive is kt above Vref (e.g. 5 → VREF+5 → add 1 × vrefPer5)
    if (vappAdditive > 0) d += vrefPer5 * (vappAdditive / 5);

    // Reverser-inop correction (add if one reverser inoperative)
    if (!reverser) d += revInop;

    results[setting] = Math.round(d / 10) * 10;
  }
  return results;
}
