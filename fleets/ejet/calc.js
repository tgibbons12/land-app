export function calcDistances({ weightLbs, flapConfig, ice, surface, elevationFt, tempC, windKt, slopePct=0, vrefAdditive=0, reverser=true }) {
  const surfKey = ["poor","wet","contaminated"].includes(surface.toLowerCase()) ? "poor" : "dry";
  const flapKey = flapConfig === "FULL FLAPS" ? "FULL FLAPS" : "FLAP 5";
  const table = QRH[`${flapKey}|${!!ice}|${surfKey}`] || QRH["FLAP 5|false|dry"];
  const weightDiff = (weightLbs - 72000) / 1000;
  const isaDev = tempC - (15 - 2 * elevationFt / 1000);
  const results = {};
  for (const [setting, vals] of Object.entries(table)) {
    const [base,wBelow,,elevCorr,isaBelow,isaAbove,hwCorr,twCorr,uphillCorr,downhillCorr,vrefCorr,revInopCorr,overweightPer1000] = vals;
    let d = base;
    d += weightDiff < 0 ? wBelow * Math.abs(weightDiff) : (overweightPer1000 ?? 0) * weightDiff;
    d += elevCorr * (elevationFt / 1000);
    d += isaDev < 0 ? isaBelow * (Math.abs(isaDev) / 5) : isaAbove * (isaDev / 5);
    d += windKt >= 0 ? hwCorr * (windKt / 5) : twCorr * (Math.abs(windKt) / 5);
    d += slopePct > 0 ? uphillCorr * slopePct : downhillCorr * Math.abs(slopePct);
    if (vrefAdditive > 0) d += vrefCorr * (vrefAdditive / 5);
    if (!reverser) d += revInopCorr;
    results[setting] = Math.round(d / 10) * 10;
  }
  return results;
}

