function lookupSpeeds(table, weightLbs, isIce) {
  for (let i = 0; i < table.length; i++) {
    if (weightLbs <= table[i][0]) {
      const r = table[i];
      if (isIce) return { VFS:r[1], VREF_FULL:r[2], VAC_FLAP2:r[3], VAC_FLAP4:r[4], VREF_FLAP5:r[5] };
      return { VFS:r[1], VREF_FLAP5:r[2], VAC_FLAP2:r[3], VREF_FULL:r[4], VAC_FLAP4:r[5] };
    }
  }
  const last = table[table.length-1];
  if (isIce) return { VFS:last[1], VREF_FULL:last[2], VAC_FLAP2:last[3], VAC_FLAP4:last[4], VREF_FLAP5:last[5] };
  return { VFS:last[1], VREF_FLAP5:last[2], VAC_FLAP2:last[3], VREF_FULL:last[4], VAC_FLAP4:last[5] };
}

